package Ex17_4;

public class Athlete {
	private String name;
	private int mobile;
	
	public Athlete(String name, int mobile) {
		this.name = name;
		this.mobile = mobile;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public int getMobile() {
		return mobile;
	}
	public void setMobile(int mobile) {
		this.mobile = mobile;
	}

}
